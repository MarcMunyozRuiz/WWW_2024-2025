function akali(){
    let cadena1 = document.getElementById("cad1");
    let cadena2 = document.getElementById("cad2");
    
    if (cadena1.style.display === "none") {
        cadena1.style.display = "block";
        cadena2.style.display = "none"
      } 
      
        else {
        cadena1.style.display = "none";
        cadena2.style.display = "block"
      }
     
}

